package icehs.science.chapter04;

public class NumberTest {

	public static void main(String[] args) {
		int a=10;
		
		System.out.println("10은 양수입니까? : "+(a>0));
		System.out.println("10은 짝수입니까? : "+(a%2==0));
		System.out.println("10은 홀수입니까? : "+(a%2!=0));
		System.out.println("10은 30보다 큽니까? : "+(a>30));

	}

}
