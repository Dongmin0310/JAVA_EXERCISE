package icehs.science.chapter04;

public class RelationalOperatorEx {

	public static void main(String[] args) {
		int a=10, b=3, c=3;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(b>=c);
		System.out.println(a==b);
		System.out.println(b==c);
		System.out.println(a!=b);
		System.out.println(b!=c);

	}

}
